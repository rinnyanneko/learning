public class J306_SumTen {

    public static void main(String args[]) {
        int count = 1;
        //前測試迴圈重覆結構
        //條件符合true時,執行下列 { 程式區塊 } 
        while (count <= 10){ 
            System.out.println("第 " + count + " 次的迴圈.");
            count++;
        }
        System.out.println("while迴圈結束......");
    }
}
